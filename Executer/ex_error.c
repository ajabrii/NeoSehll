# include "../Header/headers.h"

void ft_error(char *str, char *s1)
{
    int i;

    i = 0;
    while (str[i])
    {
        if (str[i] == '$')
        {
            ft_putstr_fd(s1, 2);
        }
        else
            ft_putchar_fd(str[i], 2);
        i++;
    }
    ft_putchar_fd('\n', 2);
}

